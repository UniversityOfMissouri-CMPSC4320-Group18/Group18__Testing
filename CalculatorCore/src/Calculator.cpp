#include "Calculator.h"

int Calculator::Add(int value_1, int value_2) { return value_1 + value_2; }
int Calculator::Subtract(int value_1, int value_2) { return value_1 - value_2; }
int Calculator::Divide(int value_1, int value_2) { return value_1 / value_2; }
int Calculator::Multiply(int value_1, int value_2) { return value_1 * value_2; }
